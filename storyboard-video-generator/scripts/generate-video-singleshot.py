def encode_data_url(path: Path) -> str:
    """Fallback: data URL encoding (works for fal-ai/openai/* image models, NOT Seedance)."""
    suffix = path.suffix.lower().lstrip(".")
    mime = {"png": "image/png", "jpg": "image/jpeg", "jpeg": "image/jpeg", "webp": "image/webp"}.get(suffix, "image/jpeg")
    with open(path, "rb") as f:
        encoded = base64.b64encode(f.read()).decode("utf-8")
    return f"data:{mime};base64,{encoded}"


def probe_duration(video_path: Path) -> Optional[float]:
    """Return the duration of a video file in seconds via ffprobe, or None on error."""
    import shutil
    import subprocess

    if not shutil.which("ffprobe"):
        return None
    try:
        result = subprocess.run(
            ["ffprobe", "-v", "error", "-show_entries", "format=duration",
             "-of", "default=noprint_wrappers=1:nokey=1", str(video_path)],
            capture_output=True, text=True, timeout=20,
        )
        return float(result.stdout.strip()) if result.returncode == 0 else None
    except Exception:
        return None


def probe_has_audio(video_path: Path) -> bool:
    """Return True if the video file has at least one audio stream."""
    import shutil
    import subprocess

    if not shutil.which("ffprobe"):
        return True  # Assume yes; ffmpeg will surface the error if not.
    try:
        result = subprocess.run(
            ["ffprobe", "-v", "error", "-select_streams", "a", "-show_entries",
             "stream=codec_type", "-of", "default=noprint_wrappers=1:nokey=1", str(video_path)],
            capture_output=True, text=True, timeout=20,
        )
        return bool(result.stdout.strip())
    except Exception:
        return True


def mix_voiceover_with_video(video_path: Path, vo_path: Path, output_path: Path,
                             fade_out_s: float = 1.5,
                             vo_volume: float = 2.5,
                             bg_volume: float = 0.8,
                             ducking_threshold: float = 0.05,
                             ducking_ratio: float = 8.0,
                             ducking_attack: int = 20,
                             ducking_release: int = 300,
                             vo_mix_weight: float = 1.6) -> bool:
    """Mix voiceover MP3 into the Seedance video's existing audio (music+SFX)."""
    import shutil
    import subprocess

    if not shutil.which("ffmpeg"):
        print("   ⚠ ffmpeg not found in PATH — install with `brew install ffmpeg`")
        return False

    # Probe duration so we can position the fade at the end
    duration = probe_duration(video_path)
    if duration is None or duration <= 0:
        # Fall back to no fade if probe fails
        afade_out = ""
        vfade_out = ""
    else:
        fade_start = max(0.0, duration - fade_out_s)
        afade_out = f",afade=t=out:st={fade_start:.2f}:d={fade_out_s:.2f}"
        # Video fade — slightly shorter, lands cleanly on the last frame
        vfade_d = max(0.5, fade_out_s - 0.3)
        vfade_start = max(0.0, duration - vfade_d)
        vfade_out = f"fade=t=out:st={vfade_start:.2f}:d={vfade_d:.2f}"

    # Detect whether the input video already has an audio track (Seedance
    # music+SFX). If not, we can't mix — we just attach the voiceover as the
    # sole audio track.
    has_audio = probe_has_audio(video_path)
    # Pad audio to exactly the video duration (apad whole_dur), so VO that's
    # shorter than video doesn't get truncated by amix duration=first.
    pad_dur = f"{duration:.3f}" if duration else "15"
    if has_audio:
        filter_complex = (
            f"[1:a]volume={vo_volume},apad=whole_dur={pad_dur},asplit=2[vo_main][vo_sc];"
            f"[0:a]volume={bg_volume}[bg];"
            f"[bg][vo_sc]sidechaincompress=threshold={ducking_threshold}:ratio={ducking_ratio}:"
            f"attack={ducking_attack}:release={ducking_release}:makeup=1[bgduck];"
            f"[bgduck][vo_main]amix=inputs=2:duration=longest:dropout_transition=0:weights=1 {vo_mix_weight},"
            f"alimiter=limit=0.95{afade_out}[aout]"
        )
        print(f"   → Sidechain ducking: vo×{vo_volume} bg×{bg_volume} ratio={ducking_ratio} thr={ducking_threshold}")
    else:
        # Silent input — VO becomes sole audio. Bump volume since no music to mask it.
        solo_volume = max(vo_volume * 0.72, 1.5)
        filter_complex = (
            f"[1:a]volume={solo_volume},apad=whole_dur={pad_dur},"
            f"alimiter=limit=0.95{afade_out}[aout]"
        )
        print(f"   → Silent input — VO sole track, volume×{solo_volume:.2f}")

    # ffmpeg can't read and write the same file in-place. If source == output,
    # write to a temp path first then move it back.
    same_file = video_path.resolve() == output_path.resolve()
    write_to = output_path.with_name(output_path.stem + ".__mix_tmp__" + output_path.suffix) if same_file else output_path

    cmd = ["ffmpeg", "-y",
           "-i", str(video_path),
           "-i", str(vo_path),
           "-filter_complex", filter_complex,
           "-map", "0:v",
           "-map", "[aout]"]
    # Apply video fade-out — re-encodes video (cannot use -c:v copy with -vf)
    if vfade_out:
        cmd += ["-vf", vfade_out, "-c:v", "libx264", "-preset", "medium", "-crf", "18", "-pix_fmt", "yuv420p"]
    else:
        cmd += ["-c:v", "copy"]
    cmd += ["-c:a", "aac", "-b:a", "192k", "-shortest", str(write_to)]

    try:
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=180)
        if result.returncode != 0:
            print(f"   ⚠ ffmpeg returncode {result.returncode}")
            print(f"   stderr (last 500 chars): {result.stderr[-500:]}")
            if write_to != output_path and write_to.exists():
                try:
                    write_to.unlink()
                except Exception:
                    pass
            return False
        # Move temp file over the original if we used a temp path
        if same_file:
            write_to.replace(output_path)
        size_mb = output_path.stat().st_size / (1024 * 1024)
        fade_note = f" (audio+video fade-out {fade_out_s}s)" if afade_out else ""
        # Sanity check — the output MUST have audio. If it doesn't, ffmpeg
        # claimed success but produced a broken file.
        if not probe_has_audio(output_path):
            print(f"   ❌ Output file has NO audio stream! ffmpeg may have lost the VO.")
            print(f"   ffmpeg cmd was: {' '.join(cmd)}")
            print(f"   stderr (last 800 chars): {result.stderr[-800:]}")
            return False
        out_dur = probe_duration(output_path)
        print(f"   ✓ Mixed audio written → {output_path.name} ({size_mb:.1f} MB, duration {out_dur:.1f}s){fade_note}")
        return True
    except Exception as e:
        print(f"   ⚠ ffmpeg mix exception: {e}")
        if write_to != output_path and write_to.exists():
            try:
                write_to.unlink()
            except Exception:
                pass
        return False


