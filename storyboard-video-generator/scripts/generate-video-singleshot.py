"""
generate-video-singleshot.py — single Seedance 2.0 call for full multi-shot video.

Compatible with Python 3.8+ (uses __future__ annotations for newer typing syntax).

Combines all cuts from video-brief.json into ONE multi-shot prompt and submits
to FAL Seedance 2.0 reference-to-video (or image-to-video) endpoint with one
reference image (product hero by default).

Use this when you want a single continuous video generated by Seedance from a
multi-shot description, rather than per-cut clips composed in ffmpeg.

Usage:
    python3 generate-video-singleshot.py \\
        --brief /documents/ads/video/[slug]/video-brief.json \\
        --output /documents/ads/video/[slug]/final-singleshot.mp4 \\
        --mode reference-to-video \\
        --tier standard

Endpoints:
    reference-to-video — reference photo as style/identity anchor (default)
    image-to-video      — reference photo as actual starting frame
"""
